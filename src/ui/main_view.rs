#[derive(Default, Debug, Clone, PartialEq)]
pub enum Focus {
    #[default]
    Main,
    LeftPanel,
    RightPanel,
    Playbar,
}

#[derive(Default, Debug, Clone, PartialEq)]
pub enum RightPanelKind {
    #[default]
    Queue,
    Lyrics,
    Related,
}

impl RightPanelKind {
    const ALL: &'static [RightPanelKind] = &[
        RightPanelKind::Queue,
        RightPanelKind::Lyrics,
        RightPanelKind::Related,
    ];

    fn label(&self) -> &'static str {
        match self {
            RightPanelKind::Queue => "Queue",
            RightPanelKind::Lyrics => "Lyrics",
            RightPanelKind::Related => "Related",
        }
    }

    fn next(&self) -> Self {
        let idx = Self::ALL.iter().position(|k| k == self).unwrap_or(0);
        Self::ALL[(idx + 1) % Self::ALL.len()].clone()
    }

    fn prev(&self) -> Self {
        let idx = Self::ALL.iter().position(|k| k == self).unwrap_or(0);
        Self::ALL[(idx + Self::ALL.len() - 1) % Self::ALL.len()].clone()
    }
}

#[derive(Clone, Debug, PartialEq)]
pub enum SidebarTarget {
    LikedSongs,
    Albums,
    Artists,
    Playlists,
    Playlist(usize),
}

#[derive(Debug)]
enum SidebarRow {
    Header(&'static str),
    Nav {
        label: &'static str,
        target: SidebarTarget,
    },
    PlaylistRow {
        idx: usize,
        name: &'static str,
        count: usize,
    },
}

impl SidebarRow {
    fn is_selectable(&self) -> bool {
        !matches!(self, SidebarRow::Header(_))
    }
}

#[derive(Debug)]
pub struct LeftSideState {
    rows: Vec<SidebarRow>,
    list_state: ListState,
}

impl Default for LeftSideState {
    fn default() -> Self {
        let mut rows: Vec<SidebarRow> = vec![
            SidebarRow::Header("Browse"),
            SidebarRow::Nav {
                label: "Liked Songs",
                target: SidebarTarget::LikedSongs,
            },
            SidebarRow::Nav {
                label: "Albums",
                target: SidebarTarget::Albums,
            },
            SidebarRow::Nav {
                label: "Artists",
                target: SidebarTarget::Artists,
            },
            SidebarRow::Nav {
                label: "Playlists",
                target: SidebarTarget::Playlists,
            },
            SidebarRow::Header("Your Playlists"),
        ];
        for (idx, p) in placeholder_playlists().iter().enumerate() {
            rows.push(SidebarRow::PlaylistRow {
                idx,
                name: p.name,
                count: p.track_count,
            });
        }
        let first = rows.iter().position(|r| r.is_selectable()).unwrap_or(0);
        let mut list_state = ListState::default();
        list_state.select(Some(first));
        Self { rows, list_state }
    }
}

impl LeftSideState {
    fn selected_idx(&self) -> usize {
        self.list_state.selected().unwrap_or(0)
    }

    fn select_next(&mut self) {
        let cur = self.selected_idx();
        let next = (cur + 1..self.rows.len()).find(|&i| self.rows[i].is_selectable());
        if let Some(i) = next {
            self.list_state.select(Some(i));
        }
    }

    fn select_prev(&mut self) {
        let cur = self.selected_idx();
        let prev = (0..cur).rev().find(|&i| self.rows[i].is_selectable());
        if let Some(i) = prev {
            self.list_state.select(Some(i));
        }
    }

    fn selected_target(&self) -> Option<SidebarTarget> {
        match &self.rows[self.selected_idx()] {
            SidebarRow::Nav { target, .. } => Some(target.clone()),
            SidebarRow::PlaylistRow { idx, .. } => Some(SidebarTarget::Playlist(*idx)),
            SidebarRow::Header(_) => None,
        }
    }
}

#[derive(Debug)]
pub struct MainPanelState {
    pub content: MainContent,
    pub table_state: TableState,
}

impl Default for MainPanelState {
    fn default() -> Self {
        let mut table_state = TableState::default();
        table_state.select(Some(0));
        Self {
            content: MainContent::default(),
            table_state,
        }
    }
}

impl MainPanelState {
    fn select_next(&mut self) {
        let max = self.content.len().saturating_sub(1);
        let cur = self.table_state.selected().unwrap_or(0);
        self.table_state.select(Some((cur + 1).min(max)));
    }

    fn select_prev(&mut self) {
        let cur = self.table_state.selected().unwrap_or(0);
        self.table_state.select(Some(cur.saturating_sub(1)));
    }
}

