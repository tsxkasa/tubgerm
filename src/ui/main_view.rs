#[derive(Debug)]
pub enum MainContent {
    Albums(Vec<Album>),
    Artists(Vec<Artist>),
    Playlists(Vec<Playlist>),
    Tracks {
        title: &'static str,
        items: Vec<Track>,
    },
}

impl Default for MainContent {
    fn default() -> Self {
        MainContent::Albums(placeholder_albums())
    }
}

impl MainContent {
    fn panel_title(&self) -> &str {
        match self {
            MainContent::Albums(_) => "Albums",
            MainContent::Artists(_) => "Artists",
            MainContent::Playlists(_) => "Playlists",
            MainContent::Tracks { title, .. } => title,
        }
    }

    fn len(&self) -> usize {
        match self {
            MainContent::Albums(v) => v.len(),
            MainContent::Artists(v) => v.len(),
            MainContent::Playlists(v) => v.len(),
            MainContent::Tracks { items, .. } => items.len(),
        }
    }
}

